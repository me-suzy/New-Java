    class InputVerifierDate extends InputVerifier{
        public boolean verify(JComponent input){
            if(input instanceof JFormattedTextField){
                JFormattedTextField ftf=(JFormattedTextField)input;
                JFormattedTextField.AbstractFormatter formatter=ftf.getFormatter();
                if(formatter!=null){
                    String text=ftf.getText();
                    if(text!=null&&!text.equals(""))
                        try{
                            Object valNoua=formatter.stringToValue(text);
                            comiteModificari(ftf,valNoua);
                            return true;
                            
                        }
                        catch(ParseException e){
                            System.out.println("Invalid input");
                            return false;
                        }
                }
            }
            return true;
        }
        public void comiteModificari(JFormattedTextField ftf,Object valNoua){
            Echipe objEchipe=(Echipe)FormEchipe.this.cboNumeech.getSelectedItem();
            if(ftf==FormEchipe.this.txtIdech)
                objEchipe.setIdech(new java.math.BigDecimal(((Number)valNoua).doubleValue()));
            else if (ftf==FormEchipe.this.txtNumeech)
                objEchipe.setNumeech((String)valNoua);
            else if (ftf==FormEchipe.this.txtCulori)
                objEchipe.setCulori((String)valNoua);
            else if (ftf==FormEchipe.this.txtTelefon)
                objEchipe.setTelefon((String)valNoua);
            else if(ftf==FormEchipe.this.txtNrjuc)
                objEchipe.setNrjuc(new java.math.BigDecimal(((Number)valNoua).doubleValue()));
            else if(ftf==FormEchipe.this.txtCodpost)
                objEchipe.setCodpost(new java.math.BigDecimal(((Number)valNoua).doubleValue()));
            else if (ftf==FormEchipe.this.txtCoddiv)
                objEchipe.setCoddiv((String)valNoua);
            
        }
        public boolean shouldYieldFocus(JComponent input){
            boolean inputValid=this.verify(input);
            if(!inputValid)
            {java.awt.Toolkit.getDefaultToolkit().beep();return false;}
            return true;
            
        }
        
    }
