    public void anuleazaModificari(Echipe objEchipa){
        if (objEchipa.getStare()==Echipe.NOU){
            FormEchipe.this.cboNumeech.removeItem(objEchipa);
        }
        else
            try{objEchipa.refresh(FormEchipe.this.conn);
            ItemListener[] lsnr=cboNumeech.getItemListeners();
            lsnr[0].itemStateChanged(new ItemEvent(cboNumeech,0,objEchipa,ItemEvent.SELECTED));
            }
            catch(Exception e){
                Utilitati.tratareErori(e);
            }
    }
